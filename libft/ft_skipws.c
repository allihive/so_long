#include "libft.h"

void	ft_skipws(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '\t' || str[i] == '\r' || str[i] == '\n'
			|| str[i] == ' ' || str[i] == '\v' || str[i] == '\f')
			i++;
		else
			return ;
	}
}
